function generateBox(array) {
	array.forEach((element)=> {
		let icon = `          
		  <div class="box">
			<i class="${element.family} ${element.prefix}${element.name}" style="color: ${element.color}"></i>
			<h5>${element.name}</h5>
		  </div>
		`;
		// stampo in html 
		$('.container').innerHTML += icon;
	})	
}